# https://github.com/JuliaReach/ReachabilityModels.jl/blob/master/src/models/ellipse
function loadEclipse()
    A = [3.0 -9.0; 4.0 -3.0]
    
    α = 0.8

    center = α * [1.0, 0.0] + (1 - α) * [1.5, 0.0];
    generators = (α * 0.1 + (1 - α) * 0.2) * Matrix(I, 2, 2);

    Z0 = Zonotope(center, generators)
    constraint = LazySets.HalfSpace([0., 1.], -5.)
    T = [0, 15]
    dimToPlot = 2

    return A, Z0, constraint, T, dimToPlot
end

